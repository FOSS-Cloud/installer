function in_list () {
    local value="$1"
    local list="$2"
    
    local item=""
    
    #echo "value: '$value'"
    #echo "list:  '$list'"

    for item in $list; do
        #echo "item: '$item'"
        if [ "$value" = "$item" ]; then
            return 0
	fi
    done

    return 1
}
